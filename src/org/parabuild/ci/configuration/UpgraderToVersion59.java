package org.parabuild.ci.configuration;

import org.apache.commons.logging.Log;
import org.apache.commons.logging.LogFactory;
import org.parabuild.ci.util.IoUtils;

import java.sql.Connection;
import java.sql.SQLException;
import java.sql.Statement;

/**
 * Upgrades to version 59.
 */
final class UpgraderToVersion59 implements SingleStepSchemaUpgrader {

  private static final Log log = LogFactory.getLog(UpgraderToVersion59.class);


  /**
   * Perform upgrade.
   */
  public void upgrade(final Connection conn, final int upgradeToVersion) throws SQLException {
    final boolean savedAutoCommit = conn.getAutoCommit();
    Statement st = null; // NOPMD
    try {
      // create statement
      conn.setAutoCommit(true);
      st = conn.createStatement();

      log.debug("Altering table");
      PersistanceUtils.executeDDLs(st, new String[]{
        " alter table RESULT_CONFIG add column FAIL_IF_NOT_FOUND char(1) default 'N' not null ",
        " update RESULT_CONFIG set FAIL_IF_NOT_FOUND='N' ",
        " alter table RESULT_CONFIG alter column FAIL_IF_NOT_FOUND drop default ",

        " alter table RESULT_CONFIG add column IGNORE_TIMESTAMP char(1) default 'N' not null ",
        " update RESULT_CONFIG set IGNORE_TIMESTAMP='N' ",
        " alter table RESULT_CONFIG alter column IGNORE_TIMESTAMP drop default ",
      });

      log.debug("Updating version");
      st.executeUpdate("update SYSTEM_PROPERTY set VALUE = '" + upgraderVersion() + "' where NAME = 'parabuild.schema.version' ");

      // finish
      conn.commit();

      // request post-startup config manager action
      System.setProperty(SystemConstants.SYSTEM_PROPERTY_INIT_ADVANCED_SETTINGS, "true");
    } finally {
      IoUtils.closeHard(st);
      conn.setAutoCommit(savedAutoCommit);
    }
  }


  public int upgraderVersion() {
    return 59;
  }
}
