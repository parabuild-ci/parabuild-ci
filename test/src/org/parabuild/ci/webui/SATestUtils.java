  public void test_isValidEmail() {
    assertTrue(!MailUtils.isValidEmail("test"));
    assertTrue(!MailUtils.isValidEmail("test.test"));
    assertTrue(!MailUtils.isValidEmail("test@test@test"));
    assertTrue(MailUtils.isValidEmail("test@test"));
    assertTrue(MailUtils.isValidEmail("test@test.com"));
  }


  /**
   * This test shows that regex validation is about 3 times
   * slower than using inet address.
   * <p/>
   * Using regex is not faesible.
   */
  public void test_isValidEmailSpeedIntetAddressVersusRegexConcept() {
    final int count = 1000;
    long start = System.currentTimeMillis();
    for (int i = 0; i < count; i++) {
      MailUtils.isValidEmail("test test com");
    }
    if (log.isDebugEnabled()) log.debug("time using InetAddress " + (System.currentTimeMillis() - start) + " ms");

    start = System.currentTimeMillis();
    for (int i = 0; i < count; i++) {
      isValidEmailUsingRegex("test test com");
    }
    if (log.isDebugEnabled()) log.debug("time using regex " + (System.currentTimeMillis() - start) + " ms");
  }


